public class Solution {
    public bool IsPalindrome(int x) {
        if(x<0)
            return false;
        int rev=0;
        int n=x;
        while(n>0){
            int number=n%10;
            rev=rev*10+number;
            n=n/10;
        }
        if(x==rev)
            return true;
            else
                return false;
    }

}